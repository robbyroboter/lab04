def count_common_elements(*lists):
    """
    Принимает произвольное количество списков и возвращает количество одинаковых элементов, встречающихся во всех списках.

    :param lists: произвольное количество списков
    :return: количество одинаковых элементов
    """
    if not lists:
        return 0  # Если списков нет, возвращаем 0

    # Используем множество для нахождения пересечения всех списков
    common_elements = set(lists[0])  # Начинаем с первого списка
    for lst in lists[1:]:
        common_elements &= set(lst)  # Пересекаем со всеми остальными списками

    return len(common_elements)  # Возвращаем количество одинаковых элементов


# Пример использования функции
list1 = [1, 2, 3, 4]
list2 = [2, 3, 5, 6]
list3 = [0, 2, 3, 8]

# Вызов функции с тремя списками
common_count = count_common_elements(list1, list2, list3)
print(f"Количество одинаковых элементов: {common_count}")
